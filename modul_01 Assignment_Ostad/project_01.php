<?php

// check number is positive or negative
$number = -4;

if($number > 0){
    echo "The number is positive.";
}elseif($number < 0){
    echo "The number is negative";
}else{
    echo "The number is zero.";
}

echo "\n";

// check this number is even or odd
if($number % 2 == 0){
    echo "This number is even.";
}else{
    echo "This number is odd.";
}

echo "\n";


